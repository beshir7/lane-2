const DICT: Record<Lang, Dict> = { en, it };

// translate(lang, key, vars?) — vars fill {placeholders} in the string, e.g.
// translate("it", "notif.raceIn", { n: 3 }) with "Gara tra {n} giorni".
export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  let s = DICT[lang]?.[key] ?? DICT.en[key] ?? key;
  if (vars) for (const k in vars) s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(vars[k]));
  return s;
}

